      CHARACTER*5 PRTVAL(31)
      CHARACTER*6 PMEAN
      CHARACTER*116 PG2LIN(2,66),NXTPG1,NXTPG2
      CHARACTER*150 NXTPG
C
C   VARIABLES TO HOLD FORMAT STATEMENTS
C
      CHARACTER*1 SETLRG(8),SETNRM(8),STRTUND(8),STPUND(8),SETSML(8)
     +           ,LINCHR(11),SETLJLPI(8),LJRESET(8)
      CHARACTER*2 RPTCNT
      CHARACTER*48 HDR1FMT,HDR3FMT,HDR4FMT,HDR5FMT
     +             ,VAL1FMT
     +             ,HD231FMT,HD232FMT,HD241FMT
     +             ,HD242FMT,HD251FMT,HD252FMT,HD262FMT
     +             ,VAL21FMT,VAL22FMT

      DATA HDR1FMT /'(6X,8A1, 6X,A52, 4X,16A1)'/
      DATA HDR3FMT /'(1X,A1,24X,A1,7X,A1,27X,A28,34X,A1)'/
      DATA HDR4FMT /'(1X,A1,1X,A20,3X,A1,A7,100A1)'/
      DATA HDR5FMT /'(1X,A1,24X,A1,7X,15(A1,1X,I2,2X),A1)'/
      DATA VAL1FMT /'(1X,A1,A24,A1,1X,A6,15(A1,A5),A1)'/ 
      DATA HD231FMT /'(1X,A1,24X,A1,7X,A1,29X,A24,17X)'/
      DATA HD232FMT /'(4(6X),A1,A7,A1)'/
      DATA HD241FMT /'(1X,A1,1X,A20,3X,A1,A7,81A1)'/
      DATA HD242FMT /'(4(6A1),A1,A7,A1)'/
      DATA HD251FMT /'(1X,A1,24X,A1,7X,12(A1,1X,I2,2X))'/
      DATA HD252FMT /'(4(A1,1X,I2,2X),A1,A7,A1)'/
      DATA HD262FMT /'(4(6A1),10A1)'/
      DATA VAL21FMT /'(1X,A1,A24,A1,A7,12(A1,A5))'/ 
      DATA VAL22FMT /'(4(A1,A5),A1,A6,A1,A1)'/ 

      DATA NUMDAYS/31,28,31,30,31,30,31,31,30,31,30,31/
      DATA OUTCNT/'        ','        '/
      OUTCNT(2)(1:1) = CHAR(0)
      PAGEFD = CHAR(12)
      DO 20 I1 = 1,31
         IDAY(I1) = I1
   20 CONTINUE

C
C   INITIALIZE COUNTERS 
C
         NRECCOUNT = 0
         RECCOUNT = 0
         IF (DETAIL.EQ.2) THEN
            LINECNT = 999
            PRTCNT = 999
         END IF
         IF (PRTCNT.GT.MAXLINE/3) THEN
            WRTHDR = 2
         ELSE
            WRTHDR = 1
         END IF
         PRTCNT = 0
C-----------------------------------------------------------------------
      DO 500 IREC=1,999999  

         CALL READDLY(DDSID,STNID,IELEM,YEAR,MONTH,VALUE,FLAG1,RTNCODE)
         RYEAR = YEAR
         RMON = MONTH
         RYRMON = RYEAR*100. + RMON
         YRMON = INT4(RYRMON)
         NRECCOUNT = NRECCOUNT + 1
         CALL LOCATE(24,15,IERR)
         WRITE(OUTCNT(1),'(I7)') NRECCOUNT
         CALL CWRITE(OUTCNT,12,IERR)
         IF (RTNCODE.EQ.'1') THEN
            GO TO 501
         ELSE IF (STNID.GT.ENDSTN.OR.(STNID.EQ.ENDSTN.AND.
     +        YRMON.GT.STRTYRMO)) THEN
            GO TO 501
         ELSE IF(STNID.LT.STARTSTN.OR.YRMON.NE.STRTYRMO) THEN
            GO TO 500
         END IF 
         IF (MONTH.EQ.2) THEN
            IF (MOD(YEAR,4).EQ.0) THEN
               NUMDAYS(MONTH) = 29
            ELSE
               NUMDAYS(MONTH) = 28
            END IF
         END IF
C
C     IF NEW ELEMENT ADD IT TO THE INTERNAL ELEMENT TABLE
C
         IF (RELELEM(IELEM).EQ.0) THEN
            CALL SETELEM(IELEM,NUMELEM,RELELEM,ELEM,ELEMABRV,ELEMNAME
     +                   ,ELEMUNITS,SMYFMT,VALFMT,' ',AVGTOT)
         END IF
C
C     SKIP THIS ELEMENT IF THAT WAS INDICATED IN THE PARAMETER FILE
C     OTHERWISE SET VARIABLE TO INDICATE THIS ELEMENT HAS BEEN READ
C
         I4 = RELELEM(IELEM)
         IF (ESKIP(I4)) THEN
            GO TO 500
         END IF
C
         IF (.NOT.PRTELM(I4)) THEN
            PRTELM(I4) = .TRUE.
            ELMCNT = ELMCNT + 1
         END IF
C
         RECCOUNT = RECCOUNT + 1 
         WRITE(OUTCNT(1),'(I7)') RECCOUNT
         CALL LOCATE (24,44,IERR)
         CALL CWRITE(OUTCNT,12,IERR)
C         
C     SET SUMMARY TYPE AND PRINT FORMAT
C
         FRMAT1 = '(F6.1)'
         FRMAT2 = '(F5.1)'
         SUMTYPE = AVGTOT(I4)
         FRMAT1(2:5) = SMYFMT(I4)
         FRMAT2(2:5) = VALFMT(I4)
C
         IF (NUMDAYS(MONTH).GT.30) THEN
            IDAY1 = 16
            HDR1FMT(17:18) = '10'
            HDR3FMT(21:22) = '30'
            HDR3FMT(29:30) = '37'
            HDR5FMT(18:19) = '16'
            VAL1FMT(21:22) = '16'
         ELSE
            IDAY1 = 15
            HDR1FMT(17:18) = ' 4'
            HDR3FMT(21:22) = '27'
            HDR3FMT(29:30) = '34'
            HDR5FMT(18:19) = '15'
            VAL1FMT(21:22) = '15'
         END IF
C
C      IF THE FIRST PAGE HAS BEEN WRITTEN WRITE THE LAST LINE OF THE
C      FIRST PAGE AND ALL OF THE SECOND DATA PAGE
C
         IF (LINECNT+2.GT.MAXLINE.OR.WRTHDR.GT.0) THEN
            IF (WRTHDR.EQ.0) THEN
               WRITE(50,200) LINCHR(7),(LINCHR(2),I5=1,24)
     +            ,LINCHR(5),(LINCHR(2),I6=1,7),(LINCHR(5),(LINCHR(2)
     +            ,I7=1,5),I8=1,IDAY1),LINCHR(8)
               LINECNT = 3
               IF (FIRSTCALL) THEN
                  WRITE(50,'(1X,8A1)') SETNRM
                  FIRSTCALL = .FALSE.
               ELSE
                  WRITE(50,'(1X,9A1)') PAGEFD,SETNRM
               END IF
               WRITE(50,'(A150)') NXTPG
               DO 300 I1 = 1,PRTCNT+1
                  WRITE(50,'(A106,A27)') (PG2LIN(J,I1),J=1,2)
                  LINECNT = LINECNT + 1
  300          CONTINUE 
               WRITE(50,200) LINCHR(7),(LINCHR(2),I5=1,24)
     +            ,LINCHR(5),(LINCHR(2),I6=1,7),(LINCHR(5),(LINCHR(2)
     +            ,I7=1,5),I8=1,IDAY3),LINCHR(5),(LINCHR(2),I6=1,7)
     +            ,LINCHR(8)
            END IF
C
C      WRITE THE HEADER LINES FOR THE NEW PAGE (PAGE 1)
C
            PREVSTN = 'XXXXXXX'
            DO 350 K = 32,1,-1
               IF (TITLE(2)(K:K).NE.' ') THEN
                  KLEN = K + 2
                  GO TO 360
               END IF
  350       CONTINUE
  360       CONTINUE
            HLDTXT = TITLE(2)
            HLDTXT(KLEN:54) = MONYEAR               
            PRTCNT = 5
            IF (WRTHDR.EQ.1) THEN
               LINECNT = LINECNT + 7
               WRITE(50,'(1X,8A1)') SETNRM
               WRITE(50,420) SETNRM,HLDTXT,SETNRM,SETSML
            ELSE
               LINECNT = 7
               IF (FIRSTCALL) THEN
                  WRITE(50,'(1X,8A1)') SETNRM
                  FIRSTCALL = .FALSE.
               ELSE
                  WRITE(50,'(1X,9A1)') PAGEFD,SETNRM
               END IF
               IPAGE = IPAGE + 1
               WRITE(50,220) SETSML,PAGNAM,IPAGE,SETNRM
               WRITE(50,HDR1FMT) SETNRM,HLDTXT,SETNRM,SETSML 
            END IF
            WRTHDR = 0
            IF (IDAY1.EQ.15) THEN
               JLINE = 89
            ELSE
               JLINE = 95
            END IF
            WRITE(50,200) LINCHR(3),(LINCHR(2),I5=1,24)
     +         ,LINCHR(6),(LINCHR(2),I6=1,7),LINCHR(6),(LINCHR(2)
     +         ,I7=1,JLINE),LINCHR(4)
            WRITE(50,HDR3FMT) (LINCHR(1),I5=1,3),DTL13,LINCHR(1)
            WRITE(50,HDR4FMT) LINCHR(1),DTL21,LINCHR(1),DTL22,LINCHR(9)
     +         ,(LINCHR(2),I5=1,5),(LINCHR(6),(LINCHR(2),I6=1,5)
     +         ,I7=2,IDAY1),LINCHR(10)
            WRITE(50,HDR5FMT) LINCHR(1),LINCHR(1),(LINCHR(1),IDAY(I)
     +         ,I=1,IDAY1),LINCHR(1)
C
C      WRITE THE HEADER FOR THE SECOND PAGE TO THE HOLD VARIABLE
C
            ISKIP = 5 + (NUMDAYS(MONTH)-28)*2
            WRITE(RPTCNT,'(I2)') ISKIP
            HDR1FMT(17:18) = RPTCNT
            PG2LIN(1,1) = ' '
            IPAGE = IPAGE + 1
            WRITE(NXTPG,220) SETSML,PAGNAM,IPAGE,SETNRM
            WRITE(NXTPG1,HDR1FMT) SETNRM,HLDTXT,SETNRM,SETSML 
            PG2LIN(1,2) = NXTPG1
            IF (IDAY1.EQ.15) THEN
               HDR1FMT(17:18) = ' 4'
            ELSE
               HDR1FMT(17:18) = '10'
            END IF
            IDAY3 = NUMDAYS(MONTH) - IDAY1
            IDAY2 = NUMDAYS(MONTH) - (IDAY1 + 12)
            WRITE(RPTCNT,'(I1)') IDAY2
            HD232FMT(2:2) = RPTCNT
            HD242FMT(2:2) = RPTCNT
            HD252FMT(2:2) = RPTCNT
            HD262FMT(2:2) = RPTCNT
            VAL22FMT(2:2) = RPTCNT

            WRITE(NXTPG1,200) LINCHR(3),(LINCHR(2),I5=1,24),LINCHR(6)
     +         ,(LINCHR(2),I6=1,7),LINCHR(6),(LINCHR(2),I7=1,71)       
            WRITE(NXTPG2,410) ((LINCHR(2),I5=1,6),I6=1,IDAY2)
     +         ,LINCHR(6),(LINCHR(2),I7=1,7),LINCHR(4)  
            PG2LIN(1,3) = NXTPG1
            PG2LIN(2,3) = NXTPG2
            WRITE(NXTPG1,HD231FMT) (LINCHR(1),I5=1,3),DTL13      
            WRITE(NXTPG2,HD232FMT) LINCHR(1),DTL14,LINCHR(1)
            PG2LIN(1,4) = NXTPG1
            PG2LIN(2,4) = NXTPG2
            WRITE(NXTPG1,HD241FMT) LINCHR(1),DTL21,LINCHR(1),DTL22
     +         ,LINCHR(9),(LINCHR(2),I5=1,5),(LINCHR(6)
     +         ,(LINCHR(2),I6=1,5),I7=1,11)       
            WRITE(NXTPG2,HD242FMT) (LINCHR(6),(LINCHR(2),I5=1,5)
     +         ,I6=1,IDAY2),LINCHR(10),DTL24,LINCHR(1)       
            PG2LIN(1,5) = NXTPG1
            PG2LIN(2,5) = NXTPG2
            WRITE(NXTPG1,HD251FMT) LINCHR(1),LINCHR(1),(LINCHR(1)
     +          ,IDAY(I),I=IDAY1+1,IDAY1+12)
            WRITE(NXTPG2,HD252FMT) (LINCHR(1),IDAY(I)
     +          ,I=IDAY1+13,NUMDAYS(MONTH)),LINCHR(1),DTL25,LINCHR(1)
            PG2LIN(1,6) = NXTPG1
            PG2LIN(2,6) = NXTPG2
         END IF 
C
C      IF NEW STATION PRINT SEPARATOR LINE AND FIND STATION INFORMATION
C
         IF (STNID.NE.PREVSTN) THEN
            LINECNT = LINECNT + 1
            PRTCNT = PRTCNT + 1
            WRITE(50,200) LINCHR(9),(LINCHR(2),I5=1,24),LINCHR(11)
     +          ,(LINCHR(2),I6=1,7),(LINCHR(11),(LINCHR(2),I7=1,5)
     +          ,I8=1,IDAY1),LINCHR(10) 
            WRITE(NXTPG1,200) LINCHR(9),(LINCHR(2),I5=1,24),LINCHR(11)
     +          ,(LINCHR(2),I6=1,7),(LINCHR(11),(LINCHR(2),I7=1,5)
     +          ,I8=1,12) 
            WRITE(NXTPG2,410) (LINCHR(11),(LINCHR(2),I5=1,5),I6=1,IDAY2)
     +          ,LINCHR(11),(LINCHR(2),I7=1,7),LINCHR(10) 
            PG2LIN(1,PRTCNT+1) = NXTPG1
            PG2LIN(2,PRTCNT+1) = NXTPG2
C
            PREVSTN = STNID
            RYEAR = YEAR
            RMONTH = MONTH
            RDATE = RYEAR*10000. + RMONTH*100. + 1.
            SRCHDATE = INT4(RDATE)
            CALL RDGEOG(STNID,SRCHDATE,STNABRV,DISTRICT,LAT,LON
     +                    ,ELEV,RTNCODE)
            IF (DETAIL.EQ.2) THEN
               WRITE(60) STNID,STNABRV,DISTRICT,LAT,LON,ELEV
               STNCNT = STNCNT + 1
            END IF 
            PRTNAM = STNABRV
         ELSE
            PRTNAM = '   '
         END IF
C
C     COMPUTE THE MONTHLY MEAN/TOTAL FOR THIS ELEMENT
C         
         TOTAL = 0.0
         ITOTAL = 0
         I4 = RELELEM(IELEM)
         DO 440 I3 = 1,NUMDAYS(MONTH)
            PRTVAL(I3) = '  -  '
            IF (VALUE(I3).NE.-99999.) THEN
               TOTAL = TOTAL + VALUE(I3)
               ITOTAL = ITOTAL + 1 
               IF (FRMAT2(2:2).EQ.'F') THEN
                  WRITE(PRTVAL(I3),FRMAT2) VALUE(I3)
               ELSE
                  IVAL = NINT(VALUE(I3))
                  WRITE(PRTVAL(I3),FRMAT2) IVAL
               END IF
            END IF
  440    CONTINUE
         IF (IELEM.EQ.005.OR.IELEM.EQ.49.OR.IELEM.EQ.50) THEN
            DO 460 I3 = 1,NUMDAYS(MONTH)
               IF (VALUE(I3).EQ.0.0) THEN
                  PRTVAL(I3) = '     '
               END IF
  460       CONTINUE
         END IF
C
C    IF NOT TOO MANY DAYS MISSING COMPUTE THE SUMMARY AND FORMAT THE
C    RESULT FOR PRINTING
C
         IMISS = NUMDAYS(MONTH) - ITOTAL
         IF (IMISS.GE.9) THEN
            PMEAN = '  -   '
         ELSE
            IF (SUMTYPE.EQ.'T') THEN
               PTYPE = 'T'
            ELSE
               PTYPE = ' '
               TOTAL = TOTAL / FLOAT(ITOTAL)
            END IF
            IF (FRMAT1(2:2).EQ.'F') THEN
               WRITE(PMEAN,FRMAT1) TOTAL
            ELSE
               NTOTAL = NINT(TOTAL)
               WRITE(PMEAN,FRMAT1) NTOTAL
            END IF   
         END IF 
         LINECNT = LINECNT + 1
         PRTCNT = PRTCNT + 1
         WRITE(50,VAL1FMT) LINCHR(1),PRTNAM,LINCHR(1),ELEMABRV(I4)
     +      ,(LINCHR(1),PRTVAL(I3),I3=1,IDAY1),LINCHR(1)
         WRITE(NXTPG1,VAL21FMT) LINCHR(1),PRTNAM,LINCHR(1),ELEMABRV(I4)
     +      ,(LINCHR(1),PRTVAL(I3),I3=IDAY1+1,IDAY1+12)
         WRITE(NXTPG2,VAL22FMT) (LINCHR(1),PRTVAL(I3)
     +      ,I3=IDAY1+13,NUMDAYS(MONTH)),LINCHR(1),PMEAN,PTYPE,LINCHR(1)
         PG2LIN(1,PRTCNT+1) = NXTPG1
         PG2LIN(2,PRTCNT+1) = NXTPG2
  500 CONTINUE
  501 CONTINUE
      IF (RECCOUNT.EQ.0) THEN
         CALL WRTMSG(5,548,12,1,1,' ',0)
         CALL LOCATE(22,0,IERR)
         STOP ' '
      END IF
C
C   END OF DATA - WRITE LAST LINE OF PAGE 1 AND ALL OF DATA PAGE 2
C
      WRITE(50,200) LINCHR(7),(LINCHR(2),I5=1,24),LINCHR(5),(LINCHR(2)
     +   ,I6=1,7),(LINCHR(5),(LINCHR(2),I7=1,5),I8=1,IDAY1),LINCHR(8)  
      LINECNT = LINECNT + 1
      IF (PRTCNT.GT.MAXLINE/2.OR.PRTCNT+LINECNT+2.GT.MAXLINE)THEN
         WRITE(50,'(1X,9A1)') PAGEFD,SETNRM
         WRITE(50,'(A150)') NXTPG
         WRITE(50,'(A106,A27)') (PG2LIN(J,1),J=1,2)
         WRITE(50,'(A106,A27)') (PG2LIN(J,2),J=1,2)
      ELSE
         IPAGE = IPAGE - 1
         WRITE(NXTPG1,'(1X,16A1)') SETNRM,SETSML
         WRITE(50,'(80X)')
      END IF
      DO 600 I1 = 3,PRTCNT+1
         WRITE(50,'(A106,A27)') (PG2LIN(J,I1),J=1,2)
         LINECNT = LINECNT + 1
  600 CONTINUE 
      WRITE(50,'(1X,140A1)') LINCHR(7),(LINCHR(2),I5=1,24),LINCHR(5)
     +   ,(LINCHR(2),I6=1,7),(LINCHR(5),(LINCHR(2),I7=1,5),I8=1,IDAY3)
     +   ,LINCHR(5),(LINCHR(2),I9=1,7),LINCHR(8)  
      LINECNT = LINECNT + 1
      RETURN
C
C   FORMAT STATEMENTS
C
  200 FORMAT(1X,132A1) 
  220 FORMAT(1X,8A1,120X,A6,1X,I2,8A1,//)
  420 FORMAT(//,4X,8A1,8X,A52,10X,16A1)
  410 FORMAT(36A1)
      END
  
