     +              ,STNABRV,PRTNAM,ELEMABRV,ELEMNAME,ELEMUNITS,MONYEAR
     +              ,SETLRG,SETNRM,SETSML,STRTUND,STPUND,LINCHR
     +              ,SETLJLPI,LJRESET
