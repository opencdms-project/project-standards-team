      CALL LOCATE(24,7,IERR)
      CALL CWRITE(LINE6,7,IERR)
      CALL LOCATE(24,59,IERR)
      CALL CWRITE(LINE7,7,IERR)
      CALL TMWAIT(200)
c
      RETURN
