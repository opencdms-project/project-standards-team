      SUBROUTINE SGTEXT
C
      CHARACTER*64 LINE1,LINE2,LINE2A,LINE3,LINE4,LINE5,LINE6,LINE7
      INTEGER*2 BGCOLOR,FGCOLOR
      INTEGER*2 STRTCOL,STRTROW,ENDCOL,ENDROW
      DATA LINE1,LINE2,LINE2A,LINE3,LINE4,LINE5,LINE6,LINE7
     + /'A United States contribution to the WMO Voluntary Cooperation '
     + ,'Program in support of the World Climate Data Program (WCDP)   '
     + ,'NOAA                            NOAA '
     + ,'National Climatic Data Center   National Weather Service  '
     + ,'Asheville, North Carolina       Silver Spring, Maryland  ' 
     + ,'USA                             USA '
     + ,'Version 3.0      '
     + ,'November 1992    '/
          
      LINE1 (62:62) = CHAR(0)
      LINE2 (62:62) = CHAR(0)
      LINE2A (62:62) = CHAR(0)
      LINE3 (62:62) = CHAR(0)
      LINE4 (62:62) = CHAR(0)
      LINE5 (62:62) = CHAR(0)
      LINE6 (16:16) = CHAR(0)
      LINE7 (22:22) = CHAR(0)

      CALL TMWAIT(50)
C      
      STRTCOL = 6
      ENDCOL = 72
      STRTROW = 11
      ENDROW = 21
      BGCOLOR = 1
      FGCOLOR = 14
C
c   DRAW THE TEXT BOX
c
      IMID = (STRTCOL + ENDCOL)/2
      CALL LOCWAIT(STRTROW,IMID,IERR,0)
      CALL CHRWRT(205,BGCOLOR,FGCOLOR,1)
      DO 80 I = STRTCOL,IMID-1
         CALL LOCWAIT(STRTROW,IMID-I+STRTCOL-1,IERR,0)
         CALL CHRWRT(205,BGCOLOR,FGCOLOR,1)
         CALL LOCATE(STRTROW,IMID+I-(STRTCOL-1),IERR)
         CALL CHRWRT(205,BGCOLOR,FGCOLOR,1)
   80 CONTINUE
      CALL LOCWAIT(STRTROW,STRTCOL,IERR,0)
      CALL CHRWRT(201,BGCOLOR,FGCOLOR,1)
      CALL LOCWAIT(STRTROW,ENDCOL,IERR,0)
      CALL CHRWRT(187,BGCOLOR,FGCOLOR,1)
      DO 120 I = STRTROW+1,ENDROW-1
         CALL LOCWAIT(I,STRTCOL,IERR,1)
         CALL CHRWRT(186,BGCOLOR,FGCOLOR,1)
         CALL LOCATE(I,ENDCOL,IERR)
         CALL CHRWRT(186,BGCOLOR,FGCOLOR,1)
  120 CONTINUE
      CALL LOCWAIT(ENDROW,STRTCOL,IERR,0)
      CALL CHRWRT(200,BGCOLOR,FGCOLOR,1)
      CALL LOCWAIT(ENDROW,ENDCOL,IERR,0)
      CALL CHRWRT(188,BGCOLOR,FGCOLOR,1)
      DO 180 I = STRTCOL,IMID-1
         CALL LOCWAIT(ENDROW,I+1,IERR,0)
         CALL CHRWRT(205,BGCOLOR,FGCOLOR,1)
         CALL LOCATE(ENDROW,ENDCOL-I+STRTCOL-1,IERR)
         CALL CHRWRT(205,BGCOLOR,FGCOLOR,1)
  180 CONTINUE
C
C   WRITE THE TEXT INSIDE THE BOX
C
      CALL TMWAIT(50)
      CALL LOCATE(13,9,IERR)
      CALL CWRITE (LINE1,15,IERR)
      CALL LOCATE(14,9,IERR)
      CALL CWRITE (LINE2,15,IERR)
      CALL TMWAIT(100)
      CALL LOCATE(16,9,IERR)
      CALL CWRITE (LINE2A,15,IERR)
      CALL LOCATE(17,9,IERR)
      CALL CWRITE (LINE3,15,IERR)
      CALL LOCATE(18,9,IERR)
      CALL CWRITE (LINE4,15,IERR)
      CALL LOCATE(19,9,IERR)
      CALL CWRITE (LINE5,15,IERR)
      CALL TMWAIT(100)
