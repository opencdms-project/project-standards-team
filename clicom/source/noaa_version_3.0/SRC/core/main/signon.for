$STORAGE:2
      PROGRAM SIGNON
C
C   THIS ROUTINE CALLs THE CLICOM SIGNON ROUTINES FOR THE AT
C
      CHARACTER*78 MSGTXT
      CHARACTER*20 FILENAME
      INTEGER*2 CHOICE, FG,BG,FG2,FG3
C
      FILENAME = 'P:\DATA\SCTRL.DAT'
      CALL DATE(IYEAR,ICOMP)
      IMON = ICOMP / 256
      IDAY = MOD(ICOMP,256)
      OPEN (5,FILE=FILENAME,STATUS='UNKNOWN'
     +       ,FORM='FORMATTED')
      READ(5,'(4I4)',END=15,ERR=90,IOSTAT=IERR) CHOICE,NDAY,NMON,NYEAR
 15   CONTINUE
      IF (IMON.EQ.NMON.AND.IDAY.EQ.NDAY.AND.IYEAR.EQ.NYEAR) THEN
         CLOSE(5)
         STOP ' '
      END IF
      REWIND (5)
      CHOICE = CHOICE + 1
      IF (CHOICE.GT.3) THEN
         CHOICE = 1
      END IF
      WRITE(5,'(4I4)') CHOICE,IDAY,IMON,IYEAR
      CLOSE(5)
      CALL CLS
C

C   READ THE COLORS FOR THE SIGNON SCREEN FROM THE CONFIG FILE
C
      IF (CHOICE.GT.0) THEN
         OPEN (10,FILE='P:\DATA\SIGNON.CFG',STATUS='OLD'
     +        ,FORM='FORMATTED')
         READ(10,20,END=25) BG,FG,FG2,FG3
   20    FORMAT(3X,I2,3(5X,I2))
         GO TO 30
   25    CONTINUE
         BG = 1
         FG = 6
         FG2 = 14
         FG3 = 15
   30    CONTINUE
         CLOSE(10)
         CALL ACTPAG(0,IERR)
         CALL CLTEXT(BG,BG,IERR)
         CALL CLRCLS(BG,FG)
      END IF
      IF (CHOICE.EQ.1) THEN
         CALL SIGN4 (FG,BG)
      ELSE IF (CHOICE.EQ.2) THEN
         CALL SIGN2 (FG,BG)
      ELSE IF (CHOICE.EQ.3) THEN
         CALL SIGN3 (FG,BG)
      END IF
      STOP ' '
C
C     ERROR PROCESSING
C
  90  CONTINUE
      WRITE(MSGTXT,100)FILENAME,' Error Code = ',IERR
 100  FORMAT (A20,2X,A14,2X,I5)
      CALL WRTMSG(4,191,12,1,0,MSGTXT,44)
      CLOSE(5)
      STOP ' '
      END
