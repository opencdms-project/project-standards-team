      SUBROUTINE TMWAIT(LENGTH)
C
C   ROUTINE TO WAIT LENGTH HUNDRETHS OF SECONDS 
C
      INTEGER*4 SEC1,NUM1,NUM2
      DATA NUM1,NUM2 /3600,60/
      CALL GETTIM(IHR,IMIN,ISEC,I100)
      SEC1 = INT4(IHR)*NUM1 + INT4(IMIN)*NUM2 + INT4(ISEC)  
      SEC1 = SEC1*100 + INT4(I100) 
      I1 = 0
50    CONTINUE
      I1 = I1 + 1
      CALL GETTIM(IHR,IMIN,ISEC,I100)
      SEC2 = INT4(IHR)*NUM1 + INT4(IMIN)*NUM2 + INT4(ISEC)  
      SEC2 = SEC2*100 + INT4(I100) 
      SECDIF = SEC2 - SEC1
      SECDIF = ABS(SECDIF)
      IF (SECDIF.LT.INT4(LENGTH)) GO TO 50
      IF (LENGTH.EQ.0.AND.I1.LT.15) GO TO 50
      RETURN
      END

